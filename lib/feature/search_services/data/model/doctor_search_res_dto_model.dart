import 'package:swasthyamap/core/utils/data_parse_helper/data_parse_helper.dart';
import 'package:swasthyamap/feature/search_services/domain/entities/doctor_search_res_dto.dart';

class DoctorSearchResDtoModel extends DoctorSearchResDto {
  const DoctorSearchResDtoModel({
    required super.doctorId,
    required super.doctorName,
    required super.specialization,
    required super.doctorPhone,
    required super.doctorEmail,
    required super.doctorImage,
    required super.experienceYears,
    required super.nextAvailableDay,
    required super.institutionId,
    required super.institutionName,
    required super.institutionType,
    required super.address,
    required super.city,
    required super.state,
    required super.institutionPhone,
    required super.institutionEmail,
    required super.dayOfWeek,
    required super.distanceKm,
    required super.availableNow,
    super.endTime,
    super.startTime,
    super.nextAvailableEndTime,
    super.nextAvailableStartTime
  });

  DoctorSearchResDtoModel copyWith({
    String? doctorId,
    String? doctorName,
    String? specialization,
    String? doctorPhone,
    String? doctorEmail,
    String? doctorImage,
    int? experienceYears,
    String? nextAvailableDay,
    DateTime? nextAvailableStartTime,
    DateTime? nextAvailableEndTime,
    String? institutionId,
    String? institutionName,
    String? institutionType,
    String? address,
    String? city,
    String? state,
    String? institutionPhone,
    String? institutionEmail,
    String? dayOfWeek,
    DateTime? startTime,
    DateTime? endTime,
    double? distanceKm,
    bool? availableNow,
  }) {
    return DoctorSearchResDtoModel(
      doctorId: doctorId ?? this.doctorId,
      doctorName: doctorName ?? this.doctorName,
      specialization: specialization ?? this.specialization,
      doctorPhone: doctorPhone ?? this.doctorPhone,
      doctorEmail: doctorEmail ?? this.doctorEmail,
      doctorImage: doctorImage ?? this.doctorImage,
      experienceYears: experienceYears ?? this.experienceYears,
      nextAvailableDay: nextAvailableDay ?? this.nextAvailableDay,
      nextAvailableStartTime: nextAvailableStartTime ??
          this.nextAvailableStartTime,
      nextAvailableEndTime: nextAvailableEndTime ?? this.nextAvailableEndTime,
      institutionId: institutionId ?? this.institutionId,
      institutionName: institutionName ?? this.institutionName,
      institutionType: institutionType ?? this.institutionType,
      address: address ?? this.address,
      city: city ?? this.city,
      state: state ?? this.state,
      institutionPhone: institutionPhone ?? this.institutionPhone,
      institutionEmail: institutionEmail ?? this.institutionEmail,
      dayOfWeek: dayOfWeek ?? this.dayOfWeek,
      startTime: startTime ?? this.startTime,
      endTime: endTime ?? this.endTime,
      distanceKm: distanceKm ?? this.distanceKm,
      availableNow: availableNow ?? this.availableNow,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'doctorId': doctorId,
      'doctorName': doctorName,
      'specialization': specialization,
      'doctorPhone': doctorPhone,
      'doctorEmail': doctorEmail,
      'doctorImage': doctorImage,
      'experienceYears': experienceYears,
      'nextAvailableDay': nextAvailableDay,
      'nextAvailableStartTime': nextAvailableStartTime?.toIso8601String(),
      'nextAvailableEndTime': nextAvailableEndTime?.toIso8601String(),
      'institutionId': institutionId,
      'institutionName': institutionName,
      'institutionType': institutionType,
      'address': address,
      'city': city,
      'state': state,
      'institutionPhone': institutionPhone,
      'institutionEmail': institutionEmail,
      'dayOfWeek': dayOfWeek,
      'startTime': startTime?.toIso8601String(),
      'endTime': endTime?.toIso8601String(),
      'distanceKm': distanceKm,
      'availableNow': availableNow,
    };
  }

  factory DoctorSearchResDtoModel.fromJson(Map<String, dynamic> map) {
    return DoctorSearchResDtoModel(
      doctorId: map['doctorId']?.toString()??"",
      doctorName: map['doctorName']?.toString()??"",
      specialization: map['specialization']?.toString()??"",
      doctorPhone: map['doctorPhone']?.toString()??"",
      doctorEmail: map['doctorEmail']?.toString()??"",
      doctorImage: map['doctorImage']?.toString()??"",
      experienceYears: DataParseHelper.safeInt(map['experienceYears']),
      nextAvailableDay: map['nextAvailableDay']?.toString()??"",
      nextAvailableStartTime: DataParseHelper.parseTime(map['nextAvailableStartTime']),
      nextAvailableEndTime: DataParseHelper.parseTime(map['nextAvailableEndTime']),
      institutionId: map['institutionId']?.toString()??"",
      institutionName: map['institutionName']?.toString()??"",
      institutionType: map['institutionType']?.toString()??"",
      address: map['address']?.toString()??"",
      city: map['city']?.toString()??"",
      state: map['state']?.toString()??"",
      institutionPhone: map['institutionPhone']?.toString()??"",
      institutionEmail: map['institutionEmail']?.toString()??"",
      dayOfWeek: map['dayOfWeek']?.toString()??"",
      startTime: DataParseHelper.parseTime(map['startTime']),
      endTime: DataParseHelper.parseTime(map['endTime']),
      distanceKm: DataParseHelper.safeDouble(map['distanceKm']),
      availableNow: map['availableNow'] == true,
    );
  }
}
